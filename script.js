//O que é SIncrono e Assincrono
//Síncrono: O código é executado linha por linha, ou seja, cada linha de código espera a anterior ser concluída antes de ser executada.
//Assíncrono: O código pode ser executado fora de ordem, ou seja, uma linha de código pode ser executada antes de outra, mesmo que a segunda linha dependa da primeira para ser concluída.

//Exemplo de código síncrono
//Executa o código linha por linha, ou seja, espera a linha de código anterior ser concluída para ser executada.

