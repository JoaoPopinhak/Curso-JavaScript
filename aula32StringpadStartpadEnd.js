let texto = '5';

//padEnd() preenche o final da string com o caractere especificado até atingir o comprimento desejado.
console.log(texto.padEnd(9, '*'));//No primeiro parametro é o comprimento desejado e no segundo parametro é o caractere que será usado para preencher a string. Se o segundo parametro for omitido, o caractere usado para preencher a string será um espaço em branco.

//padStart() preenche o início da string com o caractere especificado até atingir o comprimento desejado.
console.log(texto.padStart(9, '*'));//No primeiro parametro é o comprimento desejado e no segundo parametro é o caractere que será usado para preencher a string. Se o segundo parametro for omitido, o caractere usado para preencher a string será um espaço em branco.


