let lista = ['ovo','arroz','pipoca','macarrão'];

// Object.values() retorna os valores do array
console.log(Object.keys(lista)); // retorna as chaves do array

// Object.values() retorna os valores do array
console.log(Object.values(lista)); // retorna os valores do array

// Object.entries() retorna as chaves e os valores do array
console.log(Object.entries(lista)); // retorna as chaves e os valores do array

let pessoa = {
    nome: 'João',
    idade: 25,
    cidade: 'Criciúma'
};

// Object.keys() retorna as chaves do objeto
console.log(Object.keys(pessoa)); // retorna as chaves do objeto

// Object.values() retorna os valores do objeto
console.log(Object.values(pessoa)); // retorna os valores do objeto

// Object.entries() retorna as chaves e os valores do objeto
console.log(Object.entries(pessoa)); // retorna as chaves e os valores do objeto








